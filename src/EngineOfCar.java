public class EngineOfCar {
    String modelOfEngine;
    double capacity; // об'єм мотору
    int horsePower; // кінські сили
    int enginePrice;

    public EngineOfCar(String modelOfEngine, double capacity, int horsePower, int enginePrice){
        this.modelOfEngine = modelOfEngine;
        this.capacity = capacity;
        this.horsePower = horsePower;
        this.enginePrice = enginePrice;
    }
}
